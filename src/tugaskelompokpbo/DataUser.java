    private void btn_tambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_tambahMouseClicked
        // TODO add your handling code here:
        try{
            String sql = "INSERT INTO tb_user(`username`, `password`, `level`)"
                    + " VALUES(?, ?, ?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt_username.getText());
            pst.setString(2, txt_password.getText());
            pst.setString(3, c_level.getSelectedItem().toString());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Behasil ditambahkan");
        }catch(Exception e){
            System.out.println(e);
        }
        load_table();
    }//GEN-LAST:event_btn_tambahMouseClicked

    private void table_userMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_userMouseClicked
        // TODO add your handling code here:
         // Menampilkan data ke form
        int baris = table_user.rowAtPoint(evt.getPoint());
        String id = table_user.getValueAt(baris, 0).toString();
        txt_id.setText(id);
        String user = table_user.getValueAt(baris, 1).toString();
        txt_username.setText(user);
        String pass = table_user.getValueAt(baris, 2).toString();
        txt_password.setText(pass);
        String level = table_user.getValueAt(baris, 3).toString();
        c_level.setSelectedItem(level);
    }//GEN-LAST:event_table_userMouseClicked

