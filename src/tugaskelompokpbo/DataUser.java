    private void btn_hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_hapusMouseClicked
        // TODO add your handling code here:
        int p = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin!!!","Hapus",JOptionPane.YES_NO_OPTION);
        if(p == 0){
            try{
                String sql = "DELETE FROM tb_user WHERE id ='"+txt_id.getText()+"'";
                pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Behasil dihapus");
            }catch(Exception e){
                System.out.println(e);
            }
            load_table();
        }
    }//GEN-LAST:event_btn_hapusMouseClicked

    private void btn_ubahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ubahMouseClicked
        // TODO add your handling code here:
        try{
            String sql = "UPDATE `tb_user` SET "
                    + "`username`='"+txt_username.getText()+"',"
                    + "`password`='"+txt_password.getText()+"',"
                    + "`level`='"+c_level.getSelectedItem().toString()+"' "
                    + "WHERE id ='"+txt_id.getText()+"'";
            pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Behasil Diubah");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
            load_table();
    }//GEN-LAST:event_btn_ubahMouseClicked

